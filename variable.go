package main

import "fmt"

func main() {
	var name, uang string

	name = "Angga"
	uang = "banyak"

	fmt.Println(name)
	fmt.Println(uang)

	name = "Angga W"
	fmt.Println(name)

	// Langsung Init var tanpa tipe data
	var namaku = "angga"
	fmt.Println(namaku)

	// Ini variabel tanpa var
	namakuini := "angga"
	fmt.Println(namakuini)
}
